package Product_Test;

public class Product_pojo {

	private String pname;
	private String pdis;
	private int price;
	private int pid;
	private String pimg;
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPdis() {
		return pdis;
	}
	public void setPdis(String pdis) {
		this.pdis = pdis;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPimg() {
		return pimg;
	}
	public void setPimg(String pimg) {
		this.pimg = pimg;
	}
}
